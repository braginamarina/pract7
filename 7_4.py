group_a = ["Иванов", "Петров", "Сидоров", "Козлов", "Михайлов",
           "Федоров", "Соколов", "Алексеев", "Николаев", "Андреев"]
group_b = ["Смирнов", "Кузнецов", "Попов", "Васильев", "Зайцев",
           "Волков", "Морозов", "Новиков", "Тимофеев", "Григорьев"]

team = tuple(group_a[:5] + group_b[:5])

print(f"Группа А: {group_a}")
print(f"Группа Б: {group_b}")
print(f"Команда: {team}")

print(f"Длина кортежа: {len(team)}")

sorted_team = tuple(sorted(team))
print(f"Отсортированная команда: {sorted_team}")

